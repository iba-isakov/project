		);
		while (strlen($number)<12)
			$number="0".$number;
		$out = array();
		foreach(str_split($number,3) as $key=>$value) 
		{ 
			if (!intval($value)) 
				continue;
			$key = sizeof($unit)-$key-1; 
			$gender = $unit[$key][3];
			list($i1,$i2,$i3) = array_map('intval',str_split($value,1));
			$out[] = $hundred[$i1]; 
			if ($i2>1) 
				$out[]= $tens[$i2].' '.$ten[$gender][$i3];
			else 
				$out[]= $i2>0 ? $a20[$i3] : $ten[$gender][$i3]; 
			if ($key>1) 
				$out[]= $this->declinations($value,$unit[$key][0],$unit[$key][1],$unit[$key][2]);
		}
		$text="";
		for ($i=0; $i<count($out); $i++)
			$text.=$out[$i]." ";
		return $text;
}
 function declinations($n, $f1, $f2, $f5) {
		$n = abs(intval($n)) % 100;
		if ($n>10 && $n<20) return $f5;
		$n = $n % 10;
		if ($n>1 && $n<5) return $f2;
		if ($n==1) return $f1;
		return $f5;
 }
 public function Quad($a,$b,$c)
 {
	$x1; $x2;
		$D=$b*$b-4*$a*$c;
		if ($D<0)
			return "Корней нет";
		else 
		{
			if ($D==0)
			{
				
				return "x=".(-$b/(2*$a));
			}
			else
			{
				$x1=(-1*$b+sqrt($D))/(2*$a);
				$x2=(-1*$b-sqrt($D))/(2*$a);
				return "x1=$x1, x2=$x2";
			}
		}
 }
 public function dayOfWeek($date){
	$day = date("w", strtotime($date));
		$daysOfWeek = array ("Воскресенье", "Понедельник", "Вторник", "Среда", "Четверг", "Пятница", "Суббота");
		return $daysOfWeek[$day];
	}
	public function fibonacci($index)
	{
		if ($index==0)
			return 0;
		else if ($index<0)
		{
			return $this->fibonacci($index+2)-$this->fibonacci($index+1);
		}
		 else if($index == 1 || $index==2)
		{
			return 1;
		}
		else
		{
			return $this->fibonacci($index-2)+$this->fibonacci($index-1);
		}
	}
